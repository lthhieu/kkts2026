import { auth } from "@/auth"
import { Action, DanhmucSubject } from "@/libs/enum"
import { getUserPermission } from "@/libs/getUserPermission"
import { sendRequest } from "@/utils/api"
import { Metadata } from "next"
import { redirect } from "next/navigation"
import TablePhanloai from "@/components/csvc/danh-muc/phan-loai/table"

export const metadata: Metadata = {
    title: "Phân loại",
    description: "Generated by create next app",
}

type Params = Promise<{ current: string; pageSize: string; name?: string }>

const PhanLoaiPage = async ({ searchParams }: { searchParams: Params }) => {
    const session = await auth()
    const permission = getUserPermission(session?.user ?? {} as IUser)
    if (!permission.can(Action.Read, new DanhmucSubject())) {
        redirect('/quan-tri/trang-chu')
    }
    const { pageSize = 20, current = 1, name } = await searchParams

    const res = await sendRequest<IBackendResponse<IModelPaginate<IPhanloai>>>({
        url: `${process.env.NEXT_PUBLIC_BACKEND_URI}/phanloai`,
        queryParams: { current, pageSize, ...(name && { name: `/${name}/i` }) },
        headers: { Authorization: `Bearer ${session?.access_token}` },
        nextOption: { next: { tags: ['phanloai'] } }
    })

    return (
        <div>
            <TablePhanloai
                data={res?.data?.result ?? []}
                access_token={session?.access_token ?? ''}
                meta={res?.data?.meta!}
                user={session?.user ?? null}
            />
        </div>
    )
}
export default PhanLoaiPage
