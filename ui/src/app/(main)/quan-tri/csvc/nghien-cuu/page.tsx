import { auth } from "@/auth"
import TableNghiencuu from "@/components/csvc/nghiencuu/table"
import { Action, CsvcSubject } from "@/libs/enum"
import { getUserPermission } from "@/libs/getUserPermission"
import { sendRequest } from "@/utils/api"
import { Metadata } from "next"
import { redirect } from "next/navigation"

export const metadata: Metadata = {
    title: "Phòng nghiên cứu",
    description: "Generated by create next app",
}

type Params = Promise<{ current: string; pageSize: string, ma?: string, name?: string }>

const NghiencuuPage = async ({ searchParams }: { searchParams: Params }) => {
    const session = await auth()
    const permission = getUserPermission(session?.user ?? {} as IUser)
    if (!permission.can(Action.Read, new CsvcSubject())) {
        redirect('/quan-tri/trang-chu')
    }
    const { pageSize = 20, current = 1, ma, name } = await searchParams

    const res = await sendRequest<IBackendResponse<IModelPaginate<ICsvcSubject>>>({
        url: `${process.env.NEXT_PUBLIC_BACKEND_URI}/nghiencuu`,
        queryParams: {
            current, pageSize, ...(ma && { ma: `/${ma}/i` }),
            ...(name && { name: `/${name}/i` })
        },
        headers: { Authorization: `Bearer ${session?.access_token}` },
        nextOption: { next: { tags: ['nghiencuu'] } }
    })

    const resSummary = await sendRequest<IBackendResponse<ISummaryCsvc[]>>({
        url: `${process.env.NEXT_PUBLIC_BACKEND_URI}/nghiencuu/summary`,
        headers: { Authorization: `Bearer ${session?.access_token}` },
        nextOption: { next: { tags: ['nghiencuu'] } }
    })

    return (
        <div>
            <TableNghiencuu
                data={res?.data?.result ?? []}
                access_token={session?.access_token ?? ''}
                meta={res?.data?.meta!}
                user={session?.user ?? null}
                summary={resSummary?.data ?? null}
            />
        </div>
    )
}
export default NghiencuuPage
