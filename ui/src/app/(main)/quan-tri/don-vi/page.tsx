
import { auth } from "@/auth";
import TableUnits from "@/components/don-vi/table";
import { Action, UnitSubject } from "@/libs/enum";
import { getUserPermission } from "@/libs/getUserPermission";
import { sendRequest } from "@/utils/api";
import { Metadata } from "next";
import { redirect } from "next/navigation";

export const metadata: Metadata = {
    title: "Đơn vị",
    description: "Generated by create next app",
};

type Params = Promise<{ current: string; pageSize: string }>
const Donvi = async ({ searchParams }: { searchParams: Params }) => {
    const session = await auth()
    const permission = getUserPermission(session?.user ?? {} as IUser);
    if (!permission.can(Action.Read, new UnitSubject())) {
        redirect('/quan-tri/trang-chu')
    }
    const { pageSize = 10, current = 1 } = await searchParams
    const res = await sendRequest<IBackendResponse<IModelPaginate<IUnit>>>({
        url: `${process.env.NEXT_PUBLIC_BACKEND_URI}/units`,
        queryParams: { current, pageSize },
        headers: {
            Authorization: `Bearer ${session?.access_token}`,
        },
        nextOption: {
            next: { tags: ['units'] }
        }
    })
    return (
        <div>
            <TableUnits
                access_token={session?.access_token ?? ''}
                meta={res?.data?.meta!}
                units={res?.data?.result ?? []}
                user={session?.user ?? null} />
        </div>
    )
}
export default Donvi